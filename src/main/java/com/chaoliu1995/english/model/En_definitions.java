package com.chaoliu1995.english.model;

/**
 * 英文释义
 * @author Administrator
 *
 */
public class En_definitions {
	private String[] adj;	//形容词
	private String[] n;		//名词
	private String[] adv;	//副词
	private String[] pron;	//代词
	private String[] num;	//数词
	private String[] v;		//动词
	private String[] art;	//冠词
	private String[] prep;	//介词
	private String[] conj;	//连词
	private String[] interj;//感叹词
	
	public String[] getAdj() {
		return adj;
	}
	public void setAdj(String[] adj) {
		this.adj = adj;
	}
	public String[] getN() {
		return n;
	}
	public void setN(String[] n) {
		this.n = n;
	}
	public String[] getAdv() {
		return adv;
	}
	public void setAdv(String[] adv) {
		this.adv = adv;
	}
	public String[] getPron() {
		return pron;
	}
	public void setPron(String[] pron) {
		this.pron = pron;
	}
	public String[] getNum() {
		return num;
	}
	public void setNum(String[] num) {
		this.num = num;
	}
	public String[] getV() {
		return v;
	}
	public void setV(String[] v) {
		this.v = v;
	}
	public String[] getArt() {
		return art;
	}
	public void setArt(String[] art) {
		this.art = art;
	}
	public String[] getPrep() {
		return prep;
	}
	public void setPrep(String[] prep) {
		this.prep = prep;
	}
	public String[] getConj() {
		return conj;
	}
	public void setConj(String[] conj) {
		this.conj = conj;
	}
	public String[] getInterj() {
		return interj;
	}
	public void setInterj(String[] interj) {
		this.interj = interj;
	}
}
